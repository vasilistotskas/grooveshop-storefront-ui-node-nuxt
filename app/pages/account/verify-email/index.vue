<script lang="ts" setup>
import * as z from 'zod'

const emit = defineEmits(['emailVerify'])

const { emailVerify } = useAllAuthAuthentication()

const { t } = useI18n()
const toast = useToast()
const localePath = useLocalePath()
const { $i18n } = useNuxtApp()

const loading = ref(false)

async function onSubmit(values: EmailVerifyPostBody) {
  try {
    loading.value = true
    const data = await emailVerify(values)
    if (data && [200, 401].includes(data.status)) {
      toast.add({
        title: t('auth.email.verified'),
        color: 'success',
      })
      emit('emailVerify')
      await navigateTo(localePath('account'))
    }
  }
  catch (error) {
    handleAllAuthClientError(error)
  }
}

const formSchema = computed<DynamicFormSchema>(() => ({
  fields: [
    {
      label: t('key'),
      name: 'key',
      as: 'input',
      rules: z.string({ error: issue => issue.input === undefined
        ? $i18n.t('validation.required')
        : $i18n.t('validation.string.invalid') }),
      autocomplete: 'one-time-code',
      readonly: false,
      required: true,
      placeholder: '123456',
      type: 'text',
      condition: () => true,
      disabledCondition: () => false,
    },
  ],
}))

definePageMeta({
  layout: 'default',
  middleware: 'guest',
})
</script>

<template>
  <PageWrapper
    class="
      flex flex-col gap-4
      md:gap-8
    "
  >
    <PageTitle
      :text="t('title')"
      class="text-center capitalize"
    />
    <p
      class="
        text-center text-primary-950
        dark:text-primary-50
      "
    >
      {{ t('description') }}
    </p>
    <div class="grid items-center justify-center">
      <DynamicForm
        :button-label="$i18n.t('entry')"
        :schema="formSchema"
        class="grid"
        @submit="onSubmit"
      />
    </div>
  </PageWrapper>
</template>

<i18n lang="yaml">
el:
  title: Επιβεβαίωση Email
  description: Θα πρέπει να επιβεβαιώσεις το email σου πριν συνεχίσεις.
  key: Κωδικός
</i18n>
